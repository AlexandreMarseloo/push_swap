#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*new;

	new = malloc(sizeof(t_list));
	if (new == NULL)
		return (NULL);
	else
		new->content = content;
	new->next = NULL;
	new->rank = -1;
	return (new);
}
